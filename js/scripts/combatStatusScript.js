export function initCombatStatusUI() {
    if (!charData.combat.resistances) charData.combat.resistances = {};
    if (!charData.combat.vulnerabilities) charData.combat.vulnerabilities = {};
    if (!charData.combat.conditions) charData.combat.conditions = {};

    const resContainer = document.getElementById("damageResistancesContainer");
    const condContainer = document.getElementById("conditionsContainer");

    if (resContainer) {
        let resHtml = "";
        Object.keys(damageTypesData).forEach(key => {
            const dmg = damageTypesData[key];
            const isRes = charData.combat.resistances[key] || false;
            const isVuln = charData.combat.vulnerabilities[key] || false;
            const safeDesc = dmg.desc.replace(/"/g, '&quot;');
            let activeClass = "";
            if (isRes) activeClass = "active-res";
            else if (isVuln) activeClass = "active-vuln";

            resHtml += `
                <div class="status-badge-btn ${activeClass}" data-type="res" data-key="${key}" data-inspector="${safeDesc}">
                    <div class="dmg-icon ${dmg.class}">${dmg.icon}</div>
                    <span class="badge-label">${dmg.name}</span>
                </div>`;
        });
        resContainer.innerHTML = resHtml;
    }

    if (condContainer) {
        let condHtml = "";
        Object.keys(conditionsData).forEach(key => {
            const cond = conditionsData[key];
            const isActive = charData.combat.conditions[key] || false;
            const safeDesc = cond.desc.replace(/"/g, '&quot;');
            condHtml += `
                <div class="status-badge-btn ${isActive ? 'active-cond' : ''}" data-type="cond" data-key="${key}" data-inspector="${safeDesc}">
                    <div class="cond-icon">${cond.icon}</div>
                    <span class="badge-label">${cond.name}</span>
                </div>`;
        });
        condContainer.innerHTML = condHtml;
    }

    bindStatusEvents();
    if (typeof initInspector === "function") initInspector();
}

function bindStatusEvents() {
    document.querySelectorAll('.status-badge-btn').forEach(btn => {
        btn.onclick = () => {
            const type = btn.getAttribute('data-type');
            const key = btn.getAttribute('data-key');

            if (type === 'res') {
                const isRes = charData.combat.resistances[key];
                const isVuln = charData.combat.vulnerabilities[key];

                if (!isRes && !isVuln) {
                    charData.combat.resistances[key] = true;
                } else if (isRes) {
                    delete charData.combat.resistances[key];
                    charData.combat.vulnerabilities[key] = true;
                } else {
                    delete charData.combat.vulnerabilities[key];
                }
            } else if (type === 'cond') {
                charData.combat.conditions[key] = !charData.combat.conditions[key];
            }
            initCombatStatusUI();
        };
    });
}