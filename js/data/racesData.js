export function getDynamicRaceFeatures(raceKey, raceObj, choices = {}, lvl = 1) {
    if (!raceObj) return [];
    const features = [];

    Object.keys(raceObj.features).forEach(reqLvl => {
        if (lvl >= Number(reqLvl)) {
            raceObj.features[reqLvl].forEach(f => {
                if (raceKey === "goliath" && f.name === "Наследие великанов") {
                    const ancestry = choices.giant_ancestry || "cloud";
                    const goliathAncestryMap = {
                        cloud: { giant: "Облачный великан", skill: "Облачная прогулка", desc: "Бонусным действием вы можете магическим образом телепортироваться на расстояние до 30 футов в видимое вами незанятое пространство." },
                        fire: { giant: "Огненный великан", skill: "Огненный ожог", desc: "Когда вы попадаете по цели броском атаки и наносите ей урон, можете также нанести этой цели 1к10 урона Огнём." },
                        frost: { giant: "Ледяной великан", skill: "Морозный иней", desc: "Когда вы попадаете по цели броском атаки и наносите ей урон, можете также нанести этой цели 1к6 урона Холодом и уменьшить её Cкорость на 10 футов до начала вашего следующего хода." },
                        hill: { giant: "Холмовой великан", skill: "Обрушение холма", desc: "Когда вы попадаете по цели броском атаки и наносите ей урон, можете дать цели состояние Опрокинутый, если она Большого размера или меньше." },
                        stone: { giant: "Каменный великан", skill: "Выносливость камня", desc: "Когда вы получаете урон, можете Реакцией бросить 1к12; добавьте к результату ваш модификатор Телосложения и уменьшите урон на эту сумму." },
                        storm: { giant: "Штормовой великан", skill: "Грохот бури", desc: "Когда вы получаете урон от существа в пределах 60 футов от вас, можете Реакцией нанести этому существу 1к8 урона Звуком." }
                    };

                    const chosen = goliathAncestryMap[ancestry] || goliathAncestryMap["cloud"];
                    const addedText = `<br><br><b>${chosen.skill} (${chosen.giant}):</b> ${chosen.desc}`;

                    features.push({
                        name: `Наследие великанов: ${chosen.skill}`,
                        type: "active",
                        description: f.description + addedText
                    });
                }
                else if (raceKey === "dragonborn" && f.name === "Сопротивление урону") {
                    const ancestry = choices.dragon_ancestry || "white";
                    const dmgMap = {
                        white: "Холод", bronze: "Электричество", green: "Яд", gold: "Огонь",
                        red: "Огонь", brass: "Огонь", copper: "Кислота", silver: "Холод",
                        blue: "Электричество", black: "Кислота"
                    };
                    const dmg = dmgMap[ancestry] || "Холод";
                    features.push({
                        name: `Сопротивление урону (${dmg})`,
                        type: "passive",
                        description: `У вас есть постоянное Сопротивление к типу урона, определяемому вашей особенностью Наследие драконов: <b>${dmg}</b>.`
                    });
                }
                else if (raceKey === "dragonborn" && f.name === "Оружие дыхания") {
                    const ancestry = choices.dragon_ancestry || "white";
                    const dmgMap = {
                        white: "Холодом", bronze: "Электричеством", green: "Ядом", gold: "Огнём",
                        red: "Огнём", brass: "Огнём", copper: "Кислотой", silver: "Холодом",
                        blue: "Электричеством", black: "Кислотой"
                    };
                    const dmgText = ` (тип урона: <b>${dmgMap[ancestry] || "Холодом"}</b>)`;
                    features.push({
                        name: "Оружие дыхания",
                        type: "active",
                        description: `Когда в ваш ход вы совершаете действие Атака, можете заменить одну из ваших атак выдохом магической энергии либо в виде 15-футового Конуса, либо в виде 30-футовой Линии шириной 5 футов (на ваш выбор при каждом применении). Каждое существо в области действия должно пройти спасбросок Ловкости (Сл 8 + ваши модификатор Телосложения и Бонус владения). При провале существо получает 1к10 урона${dmgText}; его тип определяется вашей особенностью Наследие дракона. При успехе существо получает половину этого урона. Этот урон увеличивается на 1к10, когда вы достигаете 5-го (2к10), 11го (3к10) и 17-го (4к10) уровней персонажа. Вы можете использовать Оружие дыхания количество раз, равное вашему Бонусу владения. Вы восстанавливаете все потраченные использования после завершения Долгого отдыха.`
                    });
                }
                else {
                    features.push(f);
                }
            });
        }
    });
    return features;
}